package ex_03_Operators;

public class Lab_04OperatorArithmeticOperator {
    public static void main(String[] args) {
        // Arithmetic Operators
        //        + (Addition)
        //       - (Subtraction)
        //       * (Multiplication)
        //       / (Division)
        //       % (Modulus) | Modulus - Remainder
        int a = 20;
        int b = 3;
        float c = 3.56f;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a/c);

        System.out.println("a+b");
        System.out.println(a+b);

    }
}
