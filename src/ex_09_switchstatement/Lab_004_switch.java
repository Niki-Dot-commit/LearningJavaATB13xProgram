package ex_09_switchstatement;

public class Lab_004_switch {
    public static void main(String[] args) {
        char code = 'c';
        switch (code){
            default:
                System.out.println("helooooo");
                break;
            case 'A':
                System.out.println("65");
                break;
            case'B':
                System.out.println("66");
                break;
        }
    }
}
