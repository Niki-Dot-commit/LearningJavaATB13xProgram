package ex_07_Increment_decrement;

public class Lab_070Post_id {
    public static void main(String[] args) {
        int a= 10;
        System.out.println(++a);
        System.out.println(a);


        // // POST increment  = Print first and then increase
        int a_post = 10;
        System.out.println(a_post++);
        System.out.println(a_post);
    }
}
